     package Second;

     public class Encapsulation {
     private String name;
     private String idnum;
     private int age;
     
     public int getAge()
     
     {
    	 return age;
     }
     
     public String getidnum()
     
     {
    	 return idnum;
    	
     }
     public String getname()
     {
    	return name;
     }
     public void setAge(int newAge)
     {
    	 age=newAge;
     }
     public void setidnum(String idnum)
     {
    	 idnum=idnum;
     }
     public void setname(String name)
     {
    	 name=name;
     }
     
     }
